package sample;

/**
 * 計算クラス
 */
public class Calculator {
	
    /**
     * 加算処理
     * @param value1
     * @param value2
     * @return 加算結果
     */
    public int add(int value1, int value2) {
        return value1 + value2;
    }
    
    /**
     * 減算処理
     * @param value1
     * @param value2
     * @return　減算結果
     */
    public int subtract(int value1, int value2) {
        return value1 - value2;
    }
    
}
