package sample;

/**
 * 計算クラス
 */
public class Calculator {
	
    /**
     * 加算処理
     * @param a
     * @param b
     * @return 加算結果
     */
    public int add(int a, int b) {
        return a + b;
    }
    
    /**
     * 減算処理
     * @param a
     * @param b
     * @return　減算結果
     */
    public int subtract(int a, int b) {
        return a - b;
    }
    
}
